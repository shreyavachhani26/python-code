import pandas as pd

# Create two Pandas Series
s1 = pd.Series([10, 20, 30, 40, 50])
s2 = pd.Series([2, 4, 6, 8, 10])

# Addition
print("Addition:")
print(s1 + s2)

# Subtraction
print("\nSubtraction:")
print(s1 - s2)

# Multiplication
print("\nMultiplication:")
print(s1 * s2)

# Division
print("\nDivision:")
print(s1 / s2)
