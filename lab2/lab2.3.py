
# Input side length
side = float(input("Enter the side length of the cube: "))

# Calculate surface area and volume
area = 6 * (side ** 2)      # Surface area of a cube = 6a²
volume = side ** 3          # Volume of a cube = a³

# Display results
print(f"Surface Area of the cube = {area}")
print(f"Volume of the cube = {volume}")
